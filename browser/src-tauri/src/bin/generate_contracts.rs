use std::fs;
use std::path::PathBuf;

use lowband_transport_rust::{
    EngineDeckInputPayload, FetchDeckRequest, FetchDeckResponse, FetchErrorInfo, FetchTiming,
};
use ts_rs::TS;
use wavenav_host_lib::contract_types::{
    AdvanceTimeRequest, DrawCmd, EngineKey, EngineRuntimeSnapshot, HandleKeyRequest,
    LoadDeckContextRequest, LoadDeckRequest, NavigateToCardRequest, RenderList,
    ScriptDialogRequestSnapshot, ScriptTimerRequestSnapshot, SetViewportColsRequest,
};

fn push_decl<T: TS>(out: &mut String) {
    out.push_str("export ");
    out.push_str(&T::decl());
    out.push_str("\n\n");
}

fn contracts_out_dir() -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR")).join("../contracts/generated")
}

fn write_engine_contracts() -> Result<(), Box<dyn std::error::Error>> {
    let out_path = contracts_out_dir().join("engine-host.ts");
    if let Some(parent) = out_path.parent() {
        fs::create_dir_all(parent)?;
    }

    let mut output = String::new();
    output.push_str("// AUTO-GENERATED FILE. DO NOT EDIT.\n");
    output.push_str("// Generated by: cargo run --bin generate_contracts\n\n");

    push_decl::<EngineKey>(&mut output);
    push_decl::<LoadDeckRequest>(&mut output);
    push_decl::<LoadDeckContextRequest>(&mut output);
    push_decl::<HandleKeyRequest>(&mut output);
    push_decl::<NavigateToCardRequest>(&mut output);
    push_decl::<SetViewportColsRequest>(&mut output);
    push_decl::<AdvanceTimeRequest>(&mut output);
    push_decl::<ScriptDialogRequestSnapshot>(&mut output);
    push_decl::<ScriptTimerRequestSnapshot>(&mut output);
    push_decl::<EngineRuntimeSnapshot>(&mut output);
    push_decl::<DrawCmd>(&mut output);
    push_decl::<RenderList>(&mut output);

    output.push_str("export interface EngineHostClient {\n");
    output.push_str("  loadDeck(request: LoadDeckRequest): Promise<EngineRuntimeSnapshot>;\n");
    output.push_str(
        "  loadDeckContext(request: LoadDeckContextRequest): Promise<EngineRuntimeSnapshot>;\n",
    );
    output.push_str("  render(): Promise<RenderList>;\n");
    output.push_str("  handleKey(request: HandleKeyRequest): Promise<EngineRuntimeSnapshot>;\n");
    output.push_str(
        "  navigateToCard(request: NavigateToCardRequest): Promise<EngineRuntimeSnapshot>;\n",
    );
    output.push_str("  navigateBack(): Promise<EngineRuntimeSnapshot>;\n");
    output.push_str(
        "  setViewportCols(request: SetViewportColsRequest): Promise<EngineRuntimeSnapshot>;\n",
    );
    output.push_str(
        "  advanceTimeMs(request: AdvanceTimeRequest): Promise<EngineRuntimeSnapshot>;\n",
    );
    output.push_str("  snapshot(): Promise<EngineRuntimeSnapshot>;\n");
    output.push_str("  clearExternalNavigationIntent(): Promise<EngineRuntimeSnapshot>;\n");
    output.push_str("}\n");

    fs::write(&out_path, output)?;
    println!("generated {}", out_path.display());
    Ok(())
}

fn write_transport_contracts() -> Result<(), Box<dyn std::error::Error>> {
    let out_path = contracts_out_dir().join("transport-host.ts");
    let mut output = String::new();
    output.push_str("// AUTO-GENERATED FILE. DO NOT EDIT.\n");
    output.push_str("// Generated by: cargo run --bin generate_contracts\n\n");

    push_decl::<FetchDeckRequest>(&mut output);
    push_decl::<FetchTiming>(&mut output);
    push_decl::<FetchErrorInfo>(&mut output);
    push_decl::<EngineDeckInputPayload>(&mut output);
    push_decl::<FetchDeckResponse>(&mut output);

    output.push_str("export interface TransportClient {\n");
    output.push_str("  fetchDeck(request: FetchDeckRequest): Promise<FetchDeckResponse>;\n");
    output.push_str("}\n");

    fs::write(&out_path, output)?;
    println!("generated {}", out_path.display());
    Ok(())
}

fn write_tauri_client_contracts() -> Result<(), Box<dyn std::error::Error>> {
    let out_path = contracts_out_dir().join("tauri-host-client.ts");
    let output = r#"// AUTO-GENERATED FILE. DO NOT EDIT.
// Generated by: cargo run --bin generate_contracts

import type {
  EngineRuntimeSnapshot,
  HandleKeyRequest,
  LoadDeckContextRequest,
  LoadDeckRequest,
  NavigateToCardRequest,
  RenderList,
  SetViewportColsRequest,
  AdvanceTimeRequest
} from './engine-host';
import type { FetchDeckRequest, FetchDeckResponse } from './transport-host';

export type TauriInvoke = <T>(command: string, args?: Record<string, unknown>) => Promise<T>;

export interface TauriHostClient {
  health(): Promise<string>;
  fetchDeck(request: FetchDeckRequest): Promise<FetchDeckResponse>;
  engineLoadDeck(request: LoadDeckRequest): Promise<EngineRuntimeSnapshot>;
  engineLoadDeckContext(request: LoadDeckContextRequest): Promise<EngineRuntimeSnapshot>;
  engineRender(): Promise<RenderList>;
  engineHandleKey(request: HandleKeyRequest): Promise<EngineRuntimeSnapshot>;
  engineNavigateToCard(request: NavigateToCardRequest): Promise<EngineRuntimeSnapshot>;
  engineNavigateBack(): Promise<EngineRuntimeSnapshot>;
  engineSetViewportCols(request: SetViewportColsRequest): Promise<EngineRuntimeSnapshot>;
  engineAdvanceTimeMs(request: AdvanceTimeRequest): Promise<EngineRuntimeSnapshot>;
  engineSnapshot(): Promise<EngineRuntimeSnapshot>;
  engineClearExternalNavigationIntent(): Promise<EngineRuntimeSnapshot>;
}

export const createTauriHostClient = (invokeFn: TauriInvoke): TauriHostClient => ({
  health: () => invokeFn<string>('health'),
  fetchDeck: (request) => invokeFn<FetchDeckResponse>('fetch_deck', { request }),
  engineLoadDeck: (request) => invokeFn<EngineRuntimeSnapshot>('engine_load_deck', { request }),
  engineLoadDeckContext: (request) =>
    invokeFn<EngineRuntimeSnapshot>('engine_load_deck_context', { request }),
  engineRender: () => invokeFn<RenderList>('engine_render'),
  engineHandleKey: (request) => invokeFn<EngineRuntimeSnapshot>('engine_handle_key', { request }),
  engineNavigateToCard: (request) =>
    invokeFn<EngineRuntimeSnapshot>('engine_navigate_to_card', { request }),
  engineNavigateBack: () => invokeFn<EngineRuntimeSnapshot>('engine_navigate_back'),
  engineSetViewportCols: (request) =>
    invokeFn<EngineRuntimeSnapshot>('engine_set_viewport_cols', { request }),
  engineAdvanceTimeMs: (request) =>
    invokeFn<EngineRuntimeSnapshot>('engine_advance_time_ms', { request }),
  engineSnapshot: () => invokeFn<EngineRuntimeSnapshot>('engine_snapshot'),
  engineClearExternalNavigationIntent: () =>
    invokeFn<EngineRuntimeSnapshot>('engine_clear_external_navigation_intent')
});
"#;

    fs::write(&out_path, output)?;
    println!("generated {}", out_path.display());
    Ok(())
}

fn main() -> Result<(), Box<dyn std::error::Error>> {
    write_engine_contracts()?;
    write_transport_contracts()?;
    write_tauri_client_contracts()?;
    Ok(())
}
